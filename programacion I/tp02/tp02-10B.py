def es_impar(numero):
    return numero % 2 != 0

#----------------------------------------------------------------------------------------------
# CUERPO PRINCIPAL
#----------------------------------------------------------------------------------------------



numeros = []
for i in range(1,100):
    numeros.append(i)

impares = filter(es_impar, numeros)


impares_lista = list(impares)
print(impares_lista)
